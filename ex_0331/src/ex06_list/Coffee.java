package ex06_list;

public class Coffee {
	public String coffee;
	public int price;
	
	public void setCoffee(String coffee) {
		this.coffee = coffee;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getCoffee() {
		return coffee;
	}
	public int getPrice() {
		return price;
	}
}
